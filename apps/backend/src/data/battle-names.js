const ALL_BATTLE_NAMES = [...LOBSTER_NAMES, ...GENERIC_GAMING_NAMES];

// ============================================
// UTILITY FUNCTIONS
// ============================================

function getRandomBattleName() {
  return ALL_BATTLE_NAMES[Math.floor(Math.random() * ALL_BATTLE_NAMES.length)];
}

function getRandomLobsterName() {
  return LOBSTER_NAMES[Math.floor(Math.random() * LOBSTER_NAMES.length)];
}

function getRandomGenericName() {
  return GENERIC_GAMING_NAMES[Math.floor(Math.random() * GENERIC_GAMING_NAMES.length)];
}

function getRandomBattleNames(count) {
  const shuffled = [...ALL_BATTLE_NAMES].sort(() => 0.5 - Math.random());
  return shuffled.slice(0, Math.min(count, ALL_BATTLE_NAMES.length));
}

function getWeightedRandomName(lobsterWeight = 0.5) {
  if (Math.random() < lobsterWeight) {
    return getRandomLobsterName();
  }
  return getRandomGenericName();
}

function isDefaultName(name) {
  return ALL_BATTLE_NAMES.some(n => n.toLowerCase() === name.toLowerCase());
}

